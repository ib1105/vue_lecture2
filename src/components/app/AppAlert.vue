<template>
  <div class="app-alert">
    <!-- Transitoin : 나타나고 사라지는 효과 -->
    <!-- TransitoinGroup : 그룹화 시킴, ex) 여러번 클릭 -> 아래로 여러번 alert -->
    <TransitionGroup name="slide">
      <div
        v-for="({ message, type }, index) in items"
        :key="index"
        class="alert"
        :class="typeStyle(type)"
        role="alert"
      >
        {{ message }}
      </div>
    </TransitionGroup>
  </div>
</template>

<script setup>
defineProps({
  items: Array
})
const typeStyle = (type) => (type === 'error' ? 'alert-danger' : 'alert-primary')
</script>

<style scoped>
.app-alert {
  position: fixed;
  top: 10px;
  right: 10px;
}

.slide-enter-from,
.slide-leave-to {
  opacity: 0;
  transform: translateY(-30px);
}
.slide-enter-active,
.slide-leave-active {
  transition: all 0.5s ease;
}
.slide-enter-to,
.slide-leave-from {
  opacity: 1;
  transform: translateY(0px);
}
</style>
